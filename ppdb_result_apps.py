import streamlit as st
import pandas as pd
import plotly.express as px

st.set_page_config(
    page_title="Dashboard SPMB SMKN 4 Tarakan 2026",
    layout="wide"
)

st.title("Dashboard Analisis SPMB SMKN 4 Tarakan")

uploaded_file = st.file_uploader(
    "Upload File Excel Hasil SPMB",
    type=["xlsx"]
)

if uploaded_file:

    df = pd.read_excel(uploaded_file)

    st.subheader("Data SPMB")

    st.dataframe(
        df,
        hide_index=True
    )

    # ======================
    # Statistik Dasar
    # ======================

    total = len(df)

    col1, col2 = st.columns(2)

    col1.metric("Total Murid", total)
    col2.metric(
        "Jumlah Sekolah Asal",
        df["Asal Sekolah"].nunique()
    )

    # ======================
    # Distribusi Jurusan
    # ======================

    jurusan = (
        df["Kompetensi Keahlian"]
        .value_counts()
        .reset_index()
    )

    jurusan.columns = ["Jurusan", "Jumlah Murid"]

    fig = px.pie(
        jurusan,
        names="Jurusan",
        values="Jumlah Murid",
        title="Distribusi Jurusan"
    )

    st.plotly_chart(fig, use_container_width=True)

    # ======================
    # Asal Sekolah
    # ======================

    sekolah = (
        df["Asal Sekolah"]
        .value_counts()
        .reset_index()
    )

    sekolah.columns = ["Sekolah", "Jumlah Murid"]

    fig2 = px.bar(
        sekolah.head(10),
        x="Sekolah",
        y="Jumlah Murid",
        title="Top 10 Asal Sekolah"
    )

    st.plotly_chart(fig2, use_container_width=True)

    # ======================
    # Kategorisasi
    # ======================

    def kategori(x):
        if x >= 20:
            return "Sangat Tinggi"
        elif x >= 10:
            return "Tinggi"
        elif x >= 5:
            return "Sedang"
        else:
            return "Rendah"

    sekolah["Kategori"] = sekolah["Jumlah Murid"].apply(kategori)

    st.subheader("Kategori Sekolah")

    st.dataframe(
        sekolah,
        hide_index=True
    )

    # ======================
    # Insight
    # ======================

    school_count = (
        df["Asal Sekolah"]
        .value_counts()
    )

    jumlah_top = school_count.max()

    top_school_list = (
        school_count[
            school_count == jumlah_top
        ]
        .index
        .tolist()
    )

    if len(top_school_list) == 1:
        top_school_text = top_school_list[0]
    else:
        top_school_text = ", ".join(top_school_list[:-1])
        top_school_text += f" dan {top_school_list[-1]}"
    
    st.success(
        f"""
        Berdasarkan data hasil seleksi SPMB, sekolah asal dengan jumlah murid diterima tertinggi adalah
        {top_school_text}, masing-masing berkontribusi sebanyak {jumlah_top} murid.
        """
    )

    ranking = (
        df.groupby(
            ["Kompetensi Keahlian",
            "Asal Sekolah"]
        )
        .size()
        .reset_index(name="Jumlah Murid")
    )

    st.subheader("Ranking Asal Sekolah per Jurusan")

    jurusan_pilih = st.selectbox(
        "Pilih Jurusan",
        sorted(df["Kompetensi Keahlian"].unique())
    )

    hasil = (
        ranking[
            ranking["Kompetensi Keahlian"]
            == jurusan_pilih
        ]
        .sort_values(
            ["Jumlah Murid", "Asal Sekolah"],
            ascending=[False, True]
        )
    )

    st.dataframe(
        hasil,
        hide_index=True
    )

    fig = px.bar(
        hasil.head(10),
        x="Asal Sekolah",
        y="Jumlah Murid",
        title=f"Top Asal Sekolah Jurusan {jurusan_pilih}"
    )

    st.plotly_chart(
        fig,
        use_container_width=True
    )

    import plotly.express as px

    pivot = pd.pivot_table(
        df,
        index="Asal Sekolah",
        columns="Kompetensi Keahlian",
        aggfunc="size",
        fill_value=0
    )

    fig = px.imshow(
        pivot,
        text_auto=True,
        aspect="auto",
        title="Heatmap Asal Sekolah vs Jurusan"
    )

    st.plotly_chart(
        fig,
        use_container_width=True
    )

    loyal = (
        df.groupby("Asal Sekolah")
        ["Kompetensi Keahlian"]
        .nunique()
        .reset_index()
    )

    loyal.columns = [
        "Sekolah",
        "Jumlah Jurusan"
    ]

    st.subheader("Jumlah Jurusan Terhadap Asal Sekolah")

    st.dataframe(
        loyal.sort_values(
            "Jumlah Jurusan",
            ascending=False
        ),
        hide_index=True
    )

    jurusan_count = (
        df["Kompetensi Keahlian"]
        .value_counts()
    )

    max_peminat = jurusan_count.max()

    top_jurusan_list = (
        jurusan_count[
            jurusan_count == max_peminat
        ]
        .index
        .tolist()
    )

    if len(top_jurusan_list) == 1:
        top_jurusan = top_jurusan_list[0]
    else:
        top_jurusan = ", ".join(top_jurusan_list[:-1])
        top_jurusan += f" dan {top_jurusan_list[-1]}"

    st.info(
        f"""
        Sekolah penyumbang terbesar:
        {top_school_text}
        ({jumlah_top} murid)

        Jurusan dengan peminat tertinggi:
        {top_jurusan}
        masing-masing berkontribusi sebanyak {max_peminat} murid.
        """
    )

    from io import BytesIO
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
    from reportlab.lib.styles import getSampleStyleSheet

    # =====================================
    # DOWNLOAD EXCEL ANALISIS
    # =====================================

    output = BytesIO()

    with pd.ExcelWriter(output, engine="openpyxl") as writer:

        df.to_excel(
            writer,
            sheet_name="Data Asli",
            index=False
        )

        sekolah.to_excel(
            writer,
            sheet_name="Ranking Sekolah",
            index=False
        )

        jurusan.to_excel(
            writer,
            sheet_name="Ranking Jurusan",
            index=False
        )

        ranking.to_excel(
            writer,
            sheet_name="Sekolah per Jurusan",
            index=False
        )

        pivot.to_excel(
            writer,
            sheet_name="Heatmap"
        )

    excel_data = output.getvalue()

    st.download_button(
        label="Download Analisis Excel",
        data=excel_data,
        file_name="Analisis_SPMB_SMKN4.xlsx",
        mime="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
    )

    # =====================================
    # DOWNLOAD CSV
    # =====================================

    csv = sekolah.to_csv(index=False)

    st.download_button(
        label="Download Ranking Sekolah (CSV)",
        data=csv,
        file_name="ranking_sekolah.csv",
        mime="text/csv"
    )

    # =====================================
    # DOWNLOAD PDF
    # =====================================

    pdf_buffer = BytesIO()

    doc = SimpleDocTemplate(pdf_buffer)

    styles = getSampleStyleSheet()

    if len(sekolah) >= 5:

        batas = sekolah.iloc[4]["Jumlah Murid"]

        top5_sekolah = sekolah[
            sekolah["Jumlah Murid"] >= batas
        ]

    else:

        top5_sekolah = sekolah

    content = []

    content.append(
        Paragraph(
            "LAPORAN ANALISIS SPMB SMKN 4 TARAKAN",
            styles["Title"]
        )
    )

    content.append(Spacer(1, 12))

    content.append(
        Paragraph(
            f"Total Murid Diterima: {total}",
            styles["Normal"]
        )
    )

    content.append(
        Paragraph(
            f"Jumlah Sekolah Asal: {df['Asal Sekolah'].nunique()}",
            styles["Normal"]
        )
    )

    content.append(
        Paragraph(
            f"Jurusan Dengan Peminat Tertinggi: {top_jurusan} ({max_peminat} murid)",
            styles["Normal"]
        )
    )

    content.append(
        Paragraph(
            f"Sekolah Penyumbang Terbesar: {top_school_text} ({jumlah_top} murid)",
            styles["Normal"]
        )
    )

    content.append(Spacer(1, 12))

    content.append(
        Paragraph(
            "TOP 5 SEKOLAH PENYUMBANG MURID",
            styles["Heading2"]
        )
    )

    for i, row in top5_sekolah.iterrows():

        content.append(
            Paragraph(
                f"{row['Sekolah']} : {row['Jumlah']} murid",
                styles["Normal"]
            )
        )

    content.append(Spacer(1, 12))

    content.append(
        Paragraph(
            "Laporan dibuat otomatis dari Dashboard SPMB SMKN 4 Tarakan.",
            styles["Italic"]
        )
    )

    doc.build(content)

    pdf_bytes = pdf_buffer.getvalue()

    st.download_button(
        label="Download Laporan PDF",
        data=pdf_bytes,
        file_name="Laporan_SPMB_SMKN4.pdf",
        mime="application/pdf"
    )